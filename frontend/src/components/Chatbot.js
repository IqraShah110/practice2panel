import React, { useState, useRef, useEffect, useCallback } from 'react';
import { MessageCircle, X, Send, Bot, User, Mic, MicOff, Paperclip, Image as ImageIcon, FileText } from 'lucide-react';
import './Chatbot.css';
import { API_BASE_URL } from '../config';

const Chatbot = ({ currentQuestion, selectedSkill, selectedRole, interviewType, isSidebar = false, askAssistantRequest }) => {
  const [isOpen, setIsOpen] = useState(isSidebar); // Auto-open if sidebar
  const [messages, setMessages] = useState([]);
  const [inputMessage, setInputMessage] = useState('');
  const [isLoading, setIsLoading] = useState(false);
  const [isRecording, setIsRecording] = useState(false);
  const [selectedFile, setSelectedFile] = useState(null);
  const [filePreview, setFilePreview] = useState(null);
  const [fileContent, setFileContent] = useState(null);
  const messagesEndRef = useRef(null);
  const chatContainerRef = useRef(null);
  const recognitionRef = useRef(null);
  const fileInputRef = useRef(null);
  const lastAssistantRequestRef = useRef(null);

  // Initialize with welcome message
  useEffect(() => {
    if (isOpen && messages.length === 0) {
      setMessages([{
        role: 'assistant',
        content: `Hello! I'm your Interview Preparation Assistant. I can help you with:\n\n• Technical questions\n• Conceptual explanations\n• Behavioral interview tips\n• Problem-solving strategies\n• Interview preparation advice\n\nHow can I assist you today?`
      }]);
    }
  }, [isOpen]);

  // Auto scroll to bottom when new message arrives
  useEffect(() => {
    scrollToBottom();
  }, [messages]);

  const scrollToBottom = () => {
    messagesEndRef.current?.scrollIntoView({ behavior: 'smooth' });
  };

  // Function to read file content
  const readFileContent = (file) => {
    return new Promise((resolve, reject) => {
      const reader = new FileReader();
      
      reader.onload = (e) => {
        resolve(e.target.result);
      };
      
      reader.onerror = (e) => {
        reject(e);
      };
      
      if (file.type.startsWith('image/')) {
        // For images, read as base64
        reader.readAsDataURL(file);
      } else if (file.type.startsWith('text/') || file.type === 'application/pdf' || file.name.endsWith('.txt') || file.name.endsWith('.md')) {
        // For text files, read as text
        reader.readAsText(file);
      } else {
        // For other files, read as base64
        reader.readAsDataURL(file);
      }
    });
  };

  // Handle file selection
  const handleFileSelect = async (e) => {
    const file = e.target.files[0];
    if (!file) return;

    // Check file size (max 10MB)
    if (file.size > 10 * 1024 * 1024) {
      alert('File size should be less than 10MB');
      return;
    }

    setSelectedFile(file);

    try {
      const content = await readFileContent(file);
      setFileContent(content);
      
      // Create preview for images
      if (file.type.startsWith('image/')) {
        setFilePreview(URL.createObjectURL(file));
      } else {
        setFilePreview(null);
      }
    } catch (error) {
      console.error('Error reading file:', error);
      alert('Error reading file. Please try again.');
      setSelectedFile(null);
    }
  };

  // Remove selected file
  const handleRemoveFile = () => {
    setSelectedFile(null);
    setFilePreview(null);
    setFileContent(null);
    if (fileInputRef.current) {
      fileInputRef.current.value = '';
    }
  };

  // Function to send message with text
  const handleSendMessageWithText = useCallback(async (text, fileData = null) => {
    if ((!text || !text.trim()) && !fileData || isLoading) return;

    const userMessage = {
      role: 'user',
      content: text.trim() || (fileData ? `[File: ${fileData.name}]` : ''),
      file: fileData ? {
        name: fileData.name,
        type: fileData.type,
        content: fileData.content,
        preview: fileData.preview
      } : null
    };

    setMessages(prev => [...prev, userMessage]);
    const messageToSend = text.trim() || 'Please analyze this file';
    setInputMessage('');
    setIsLoading(true);

    // Clear file after sending
    if (fileData) {
      setSelectedFile(null);
      setFilePreview(null);
      setFileContent(null);
      if (fileInputRef.current) {
        fileInputRef.current.value = '';
      }
    }

    try {
      const requestBody = {
        message: messageToSend,
        file: fileData ? {
          name: fileData.name,
          type: fileData.type,
          content: fileData.content
        } : null,
        context: {
          currentQuestion: currentQuestion || null,
          skill: selectedSkill || null,
          role: selectedRole || null,
          interviewType: interviewType || null
        },
        conversationHistory: messages.slice(-10) // Last 10 messages for context
      };

      console.log('Sending chatbot request to:', `${API_BASE_URL}/api/chatbot`);
      console.log('Request body:', requestBody);

      const response = await fetch(`${API_BASE_URL}/api/chatbot`, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Accept': 'application/json',
        },
        body: JSON.stringify(requestBody)
      });

      console.log('Response status:', response.status);
      console.log('Response headers:', Object.fromEntries(response.headers.entries()));

      if (!response.ok) {
        const errorText = await response.text();
        console.error('Error response:', errorText);
        throw new Error(`HTTP error! status: ${response.status}, message: ${errorText}`);
      }

      const data = await response.json();
      console.log('Response data:', data);

      if (data.success) {
        setMessages(prev => [...prev, {
          role: 'assistant',
          content: data.response
        }]);
      } else {
        setMessages(prev => [...prev, {
          role: 'assistant',
          content: data.message || 'Sorry, I encountered an error. Please try again.'
        }]);
      }
    } catch (error) {
      console.error('Chatbot error:', error);
      let errorMessage = 'Sorry, I couldn\'t connect to the server. ';
      
      if (error.message.includes('Failed to fetch') || error.message.includes('NetworkError')) {
        errorMessage += 'Please make sure the backend server is running on port 5000.';
      } else if (error.message.includes('HTTP error')) {
        errorMessage += 'Server returned an error. Please try again.';
      } else {
        errorMessage += 'Please check your connection and try again.';
      }
      
      setMessages(prev => [...prev, {
        role: 'assistant',
        content: errorMessage
      }]);
    } finally {
      setIsLoading(false);
    }
  }, [messages, isLoading, currentQuestion, selectedSkill, selectedRole, interviewType]);

  useEffect(() => {
    if (!askAssistantRequest) return;
    if (lastAssistantRequestRef.current === askAssistantRequest.timestamp) return;

    const { question, context } = askAssistantRequest;
    if (!question) return;

    lastAssistantRequestRef.current = askAssistantRequest.timestamp;

    const skillLabel = context?.skill || 'General';
    const roleLabel = context?.role || 'Candidate';
    const interviewLabel = context?.interviewType || 'Conceptual';

    const prompt = `Question: ${question}\nSkill: ${skillLabel}\nRole: ${roleLabel}\nInterview Type: ${interviewLabel}\n\nPlease help me understand this question and provide guidance on how to answer it effectively.`;

    const sendPrompt = () => handleSendMessageWithText(prompt);

    if (!isOpen) {
      setIsOpen(true);
      setTimeout(sendPrompt, 250);
    } else {
      sendPrompt();
    }
  }, [askAssistantRequest, handleSendMessageWithText, isOpen]);

  // Initialize speech recognition
  useEffect(() => {
    if ('webkitSpeechRecognition' in window || 'SpeechRecognition' in window) {
      const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
      recognitionRef.current = new SpeechRecognition();
      recognitionRef.current.continuous = false;
      recognitionRef.current.interimResults = false;
      recognitionRef.current.lang = 'en-US';

      recognitionRef.current.onresult = (event) => {
        const transcript = event.results[0][0].transcript;
        setInputMessage(transcript);
        setIsRecording(false);
        // Auto-send message after voice input
        setTimeout(() => {
          if (transcript.trim()) {
            handleSendMessageWithText(transcript);
          }
        }, 500);
      };

      recognitionRef.current.onerror = (event) => {
        console.error('Speech recognition error:', event.error);
        setIsRecording(false);
      };

      recognitionRef.current.onend = () => {
        setIsRecording(false);
      };
    }

    return () => {
      if (recognitionRef.current) {
        recognitionRef.current.stop();
      }
    };
  }, [handleSendMessageWithText]);

  const handleVoiceToggle = () => {
    if (isRecording) {
      // Stop recording
      if (recognitionRef.current) {
        recognitionRef.current.stop();
      }
      setIsRecording(false);
    } else {
      // Start recording
      if (recognitionRef.current) {
        try {
          recognitionRef.current.start();
          setIsRecording(true);
        } catch (error) {
          console.error('Error starting speech recognition:', error);
        }
      }
    }
  };

  const handleSendMessage = async () => {
    if ((!inputMessage.trim() && !selectedFile) || isLoading) return;
    
    const fileData = selectedFile ? {
      name: selectedFile.name,
      type: selectedFile.type,
      content: fileContent,
      preview: filePreview
    } : null;
    
    await handleSendMessageWithText(inputMessage, fileData);
  };

  const handleKeyPress = (e) => {
    if (e.key === 'Enter' && !e.shiftKey) {
      e.preventDefault();
      handleSendMessage();
    }
  };

  const clearChat = () => {
    setMessages([{
      role: 'assistant',
      content: `Hello! I'm your Interview Preparation Assistant. I can help you with:\n\n• Technical questions\n• Conceptual explanations\n• Behavioral interview tips\n• Problem-solving strategies\n• Interview preparation advice\n\nHow can I assist you today?`
    }]);
    handleRemoveFile();
  };

  return (
    <>
      {/* Floating Chat Button - Only show if not sidebar */}
      {!isSidebar && !isOpen && (
        <button
          className="chatbot-toggle-btn"
          onClick={() => setIsOpen(true)}
          aria-label="Open chatbot"
        >
          <MessageCircle size={24} />
          <span className="chatbot-badge">AI Assistant</span>
        </button>
      )}

      {/* Chat Window */}
      {isOpen && (
        <div className={`chatbot-container ${isSidebar ? 'chatbot-sidebar-mode' : ''}`}>
          <div className="chatbot-header">
            <div className="chatbot-header-content">
              <Bot size={20} />
              <div>
                <h3>Interview Assistant</h3>
                <p className="chatbot-subtitle">Ask me anything about interview preparation</p>
              </div>
            </div>
            <div className="chatbot-header-actions">
              <button
                className="chatbot-clear-btn"
                onClick={clearChat}
                title="Clear chat"
              >
                Clear
              </button>
              {!isSidebar && (
                <button
                  className="chatbot-close-btn"
                  onClick={() => setIsOpen(false)}
                  aria-label="Close chatbot"
                >
                  <X size={20} />
                </button>
              )}
            </div>
          </div>

          <div className="chatbot-messages" ref={chatContainerRef}>
            {messages.map((msg, index) => (
              <div
                key={index}
                className={`chatbot-message ${msg.role === 'user' ? 'user-message' : 'assistant-message'}`}
              >
                <div className="message-avatar">
                  {msg.role === 'user' ? <User size={16} /> : <Bot size={16} />}
                </div>
                <div className="message-content">
                  {msg.file && (
                    <div className="message-file-preview">
                      {msg.file.preview ? (
                        <div className="file-preview-image">
                          <img src={msg.file.preview} alt={msg.file.name} />
                          <div className="file-name">{msg.file.name}</div>
                        </div>
                      ) : (
                        <div className="file-preview-document">
                          <FileText size={24} />
                          <div className="file-name">{msg.file.name}</div>
                        </div>
                      )}
                    </div>
                  )}
                  {msg.content && (
                    <div className="message-text">
                      {msg.content.split('\n').map((line, i) => (
                        <React.Fragment key={i}>
                          {line}
                          {i < msg.content.split('\n').length - 1 && <br />}
                        </React.Fragment>
                      ))}
                    </div>
                  )}
                </div>
              </div>
            ))}
            {isLoading && (
              <div className="chatbot-message assistant-message">
                <div className="message-avatar">
                  <Bot size={16} />
                </div>
                <div className="message-content">
                  <div className="typing-indicator">
                    <span></span>
                    <span></span>
                    <span></span>
                  </div>
                </div>
              </div>
            )}
            <div ref={messagesEndRef} />
          </div>

          {selectedFile && (
            <div className="chatbot-file-preview-container">
              <div className="chatbot-file-preview">
                {filePreview ? (
                  <div className="file-preview-image">
                    <img src={filePreview} alt={selectedFile.name} />
                    <span className="file-name">{selectedFile.name}</span>
                  </div>
                ) : (
                  <div className="file-preview-document">
                    <FileText size={20} />
                    <span className="file-name">{selectedFile.name}</span>
                  </div>
                )}
                <button
                  className="file-remove-btn"
                  onClick={handleRemoveFile}
                  title="Remove file"
                >
                  <X size={16} />
                </button>
              </div>
            </div>
          )}
          <div className="chatbot-input-container">
            <input
              type="file"
              ref={fileInputRef}
              className="chatbot-file-input"
              accept="image/*,.pdf,.txt,.doc,.docx,.md"
              onChange={handleFileSelect}
              style={{ display: 'none' }}
            />
            <input
              type="text"
              className="chatbot-input"
              placeholder={isRecording ? "Listening..." : selectedFile ? "Ask about the file..." : "Type your question..."}
              value={inputMessage}
              onChange={(e) => setInputMessage(e.target.value)}
              onKeyPress={handleKeyPress}
              disabled={isLoading || isRecording}
            />
            <button
              className={`chatbot-voice-btn ${isRecording ? 'recording' : ''}`}
              onClick={handleVoiceToggle}
              title={isRecording ? 'Stop recording' : 'Start voice input'}
              style={{
                display: 'flex',
                visibility: 'visible',
                opacity: 1,
                width: '44px',
                height: '44px',
                minWidth: '44px',
                minHeight: '44px',
                backgroundColor: '#f0f0f0',
                border: '2px solid #667eea',
                borderRadius: '50%',
                cursor: 'pointer',
                alignItems: 'center',
                justifyContent: 'center',
                flexShrink: 0,
                zIndex: 100
              }}
            >
              {isRecording ? <MicOff size={18} /> : <Mic size={18} />}
            </button>
            <button
              className="chatbot-file-btn"
              onClick={() => fileInputRef.current?.click()}
              title="Upload file or image"
              style={{
                display: 'flex',
                visibility: 'visible',
                opacity: 1,
                width: '44px',
                height: '44px',
                minWidth: '44px',
                minHeight: '44px',
                backgroundColor: '#f0f0f0',
                border: '2px solid #667eea',
                borderRadius: '50%',
                cursor: 'pointer',
                alignItems: 'center',
                justifyContent: 'center',
                flexShrink: 0,
                zIndex: 100
              }}
            >
              <Paperclip size={18} />
            </button>
            <button
              className="chatbot-send-btn"
              onClick={handleSendMessage}
              disabled={(!inputMessage.trim() && !selectedFile) || isLoading}
            >
              <Send size={18} />
            </button>
          </div>
        </div>
      )}
    </>
  );
};

export default Chatbot;

